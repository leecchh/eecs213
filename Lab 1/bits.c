/*
 * ilog2 - return floor(log base 2 of x), where x > 0
 *   Example: ilog2(16) = 4
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 90
 *   Rating: 4
 */
int ilog2(int x) {
  int cons1=0x7F<<24|0xFF<<16|0xFF<<8|0xFF;
  int cons2=cons1+~(0x4<<28)+1;
  int cons3=cons2+~(0x3<<28)+1;
  int cons4=cons3+~(0xF<<24)+1;
  int cons5=cons4+~(0xFF<<16)+1;
  int cons6=0x55<<24|0x55<<16|0x55<<8|0x55;
  int cons7=0x33<<24|0x33<<16|0x33<<8|0x33; 
  int cons8=0x0F<<24|0x0F<<16|0x0F<<8|0x0F;
  int num=0;
  x=x |((x >> 1)&cons1);//arit shift
  x=x |((x >> 2)&cons2);
  x=x |((x >> 4)&cons3);
  x=x |((x >> 8)&cons4);
  x=x |((x >> 16)&cons5);
  x=x+~((x>>1)&cons6)+1;
  x=((x)&cons7)+((x>>2)&cons7);
  num=(x+(x>>4))&cons8;
  return ((num+(num<<8)+(num<<16)+(num<<24))>>24)+~0;
}
